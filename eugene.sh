## Creating a Remote Repo ####
=======================================================================
steps
=======================================================================
You will have to create a remote repo on your github account. 

-- Click on new repository
-- give it a name 
-- give it a description
-- You can choose either public or private repo 
-- initialize with a readme file 
-- create repo 

--= You have created your git repository
-- Copy the http link of the repo 

Once you're done, copy the https url and run:
==> git remote add gitclassrepo https://github.com/richkingss/gitclassrepo.git

==> git remote 
# This will show all your remote repositories
==================================================================

